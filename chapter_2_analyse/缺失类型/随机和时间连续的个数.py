def count_values(data_dict):
    """
    统计字典中 key=1 的值和其他 key 的值的总和，并返回一个列表。

    :param data_dict: 输入的字典数据
    :return: 一个包含两个变量的列表，第一个变量是 key=1 的值，第二个变量是其他 key 的值的总和
    """
    # 初始化统计变量
    value_1 = 0
    sum_others = 0

    # 遍历字典，统计值
    for key, value in data_dict.items():
        if key == 1:
            value_1 = value
        else:
            sum_others += value

    return [value_1, sum_others]

